package CollectionObjects;

/**
 * Перечисление UnitOfMeasure представляет единицы измерения.
 * Возможные значения:
 * <ul>
 *     <li>KILOGRAMS - килограммы.</li>
 *     <li>METERS - метры.</li>
 *     <li>SQUARE_METERS - квадратные метры.</li>
 *     <li>MILLILITERS - миллилитры.</li>
 *     <li>MILLIGRAMS - миллиграммы.</li>
 * </ul>
 */
public enum UnitOfMeasure {
    /** Единица измерения: килограммы. */
    KILOGRAMS,

    /** Единица измерения: метры. */
    METERS,

    /** Единица измерения: квадратные метры. */
    SQUARE_METERS,

    /** Единица измерения: миллилитры. */
    MILLILITERS,

    /** Единица измерения: миллиграммы. */
    MILLIGRAMS
}